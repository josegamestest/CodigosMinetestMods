controles do jogador que podem se acessados no minetest usando a função get_player_control()
Alguns dos campos comuns disponíveis na tabela de controles:
    up: Verdadeiro se a tecla para frente estiver pressionada.
    down: Verdadeiro se a tecla para trás estiver pressionada.
    left: Verdadeiro se a tecla para a esquerda estiver pressionada.
    right: Verdadeiro se a tecla para a direita estiver pressionada.
    jump: Verdadeiro se a tecla de pulo estiver pressionada.
    sneak: Verdadeiro se a tecla de agachamento/andar devagar estiver pressionada.
    LMB (Left Mouse Button): Verdadeiro se o botão esquerdo do mouse estiver pressionado.
    RMB (Right Mouse Button): Verdadeiro se o botão direito do mouse estiver pressionado.
